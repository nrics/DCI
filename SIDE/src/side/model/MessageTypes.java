package side.model;

public class MessageTypes {
	
	String messageName;
	int numberOfComplexTypes;
	int numberOfSimpleTypes;

	public String getMessageName() {
		return messageName;
	}

	public void setMessageName(String messageName) {
		this.messageName = messageName;
	}
	
	public int getNumberOfComplexTypes() {
		return numberOfComplexTypes;
	}

	public void setNumberOfComplexTypes(int numberOfComplexTypes) {
		this.numberOfComplexTypes = numberOfComplexTypes;
	}

	public int getNumberOfSimpleTypes() {
		return numberOfSimpleTypes;
	}

	public void setNumberOfSimpleTypes(int numberOfSimpleTypes) {
		this.numberOfSimpleTypes = numberOfSimpleTypes;
	}
	

}
